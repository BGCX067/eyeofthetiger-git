    public void setBarcodeVisible(boolean visible) {
        if(visible) {
            jPanelBarcode.removeAll();
            jPanelBarcode.add(Utils.BarcodeFor(participant.getNumero()),BorderLayout.CENTER);
            jPanelBarcode.setVisible(true);
            this.updateUI();
        }
        else {
            jPanelBarcode.removeAll();
            jPanelBarcode.setVisible(false);
            this.updateUI();
        }
    }
    
    
