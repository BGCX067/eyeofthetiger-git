    public static abstract class CreateParticipantPanelWorker extends SwingWorker<Void,Object> {
        
        public CreateParticipantPanelWorker(Participant p) {
            participant = p;
        }
        
        Participant participant;
        ParticipantPanel participantPanel;
        @Override
        public Void doInBackground() {
            participantPanel = new ParticipantPanel(participant);
            participantPanel.setBarcodeVisible(true);
            return null;
        }

       @Override
       protected void done() {
           try { 
               use(participantPanel);
           } catch (Exception ignore) {
           }
       }
       
       protected abstract void use(ParticipantPanel pp);
    }    

    
    
    
